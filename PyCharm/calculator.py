class calculator:
    def addition(self, x, y):
        added = x + y
        print(added)

    def subtraction(self, x, y):
        sub = x - y
        print(sub)

    def multiplication(self, x, y):
        mult = x * y
        print(mult)

    def division(self, x, y):
        div = x / y
        print(div)



d = calculator()
d.addition(2,3)

ds = d.subtraction
ds(5,1)

